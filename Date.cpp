#include "Date.h"

//getters and settes for date class
void Date::setMonth(string m){
	month = m;
}
string Date::getMonth(){
	return month;
}
void Date::setDay(string d){
	day = d;
}
string Date::getDay(){
	return day;
}
void Date::setYear(string y){
	year = y;
}
string Date::getYear(){
	return year;
}